import { IsOptional, IsString, IsEmail } from 'class-validator';

export class UserInfoDTO {
  @IsOptional()
  @IsString()
  id?: string;

  @IsOptional()
  @IsString()
  name?: string;

  @IsOptional()
  @IsEmail()
  email?: string;

  @IsOptional()
  @IsString()
  password?: string;

  @IsOptional()
  picture?: string | undefined;
}
